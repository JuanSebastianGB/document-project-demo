/**
 * Add Two numbers
 * @param {number} number1 First Number Number
 * @param {number} number2 Second Number
 * @returns {number | string} sum of number1 and number2
 */
const add = (number1, number2) => number1 + number2;

/**
 * Substract two numbers
 * @param {number} number1 First Number
 * @param {number} number2 Second Number
 * @returns {number | string} substraction of number1 with number2
 */
const substract = (number1, number2) => number1 - number2;